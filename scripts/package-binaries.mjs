import { mkdir, mkdtemp, rm, writeFile } from "node:fs/promises";
import { tmpdir } from "node:os";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { spawnSync } from "node:child_process";

const rootDir = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const outputDir = join(rootDir, "bin", "platform");
const cliEntryFromOutput = "../../packages/cli/dist/index.js";

const launcherSource = (platformName) => `#!/usr/bin/env node
import { main } from "${cliEntryFromOutput}";

process.env.STORYOS_PACKAGED_PLATFORM = "${platformName}";
await main(process.argv.slice(2));
`;

await mkdir(outputDir, {
  recursive: true
});

await writeFile(join(outputDir, "storyctl-macos"), launcherSource("macos"), {
  encoding: "utf8",
  mode: 0o755
});
await writeFile(join(outputDir, "storyctl-linux"), launcherSource("linux"), {
  encoding: "utf8",
  mode: 0o755
});

const csharpSource = String.raw`
using System;
using System.Diagnostics;
using System.IO;
using System.Reflection;
using System.Text;

public static class StoryctlLauncher
{
    public static int Main(string[] args)
    {
        string exeDir = Path.GetDirectoryName(Assembly.GetExecutingAssembly().Location);
        string cliEntry = Path.GetFullPath(Path.Combine(exeDir, "..", "..", "packages", "cli", "dist", "index.js"));
        string arguments = Quote(cliEntry);

        foreach (string arg in args)
        {
            arguments += " " + Quote(arg);
        }

        try
        {
            Process process = new Process();
            process.StartInfo.FileName = "node";
            process.StartInfo.Arguments = arguments;
            process.StartInfo.UseShellExecute = false;
            process.Start();
            process.WaitForExit();
            return process.ExitCode;
        }
        catch (Exception error)
        {
            Console.Error.WriteLine("StoryMaker platform launcher requires Node.js 20 or newer on PATH.");
            Console.Error.WriteLine(error.Message);
            return 1;
        }
    }

    private static string Quote(string value)
    {
        if (value.Length == 0)
        {
            return "\"\"";
        }

        if (value.IndexOfAny(new char[] { ' ', '\t', '\n', '\v', '"' }) < 0)
        {
            return value;
        }

        StringBuilder result = new StringBuilder();
        result.Append('"');

        int backslashes = 0;
        foreach (char character in value)
        {
            if (character == '\\')
            {
                backslashes++;
                continue;
            }

            if (character == '"')
            {
                result.Append('\\', backslashes * 2 + 1);
                result.Append('"');
                backslashes = 0;
                continue;
            }

            if (backslashes > 0)
            {
                result.Append('\\', backslashes);
                backslashes = 0;
            }

            result.Append(character);
        }

        if (backslashes > 0)
        {
            result.Append('\\', backslashes * 2);
        }

        result.Append('"');
        return result.ToString();
    }
}
`;

if (process.platform === "win32") {
  const tempDir = await mkdtemp(join(tmpdir(), "storymaker-launcher-"));
  const sourcePath = join(tempDir, "StoryctlLauncher.cs");
  const outputPath = join(outputDir, "storyctl.exe");

  try {
    await writeFile(sourcePath, csharpSource, "utf8");
    const compile = spawnSync(
      "powershell.exe",
      [
        "-NoProfile",
        "-ExecutionPolicy",
        "Bypass",
        "-Command",
        `Add-Type -Path '${sourcePath.replaceAll("'", "''")}' -OutputAssembly '${outputPath.replaceAll("'", "''")}' -OutputType ConsoleApplication`
      ],
      {
        cwd: rootDir,
        encoding: "utf8"
      }
    );

    if (compile.status !== 0) {
      throw new Error(`Failed to compile storyctl.exe.\n${compile.stdout}\n${compile.stderr}`);
    }
  } finally {
    await rm(tempDir, {
      force: true,
      recursive: true
    });
  }
} else {
  await writeFile(join(outputDir, "storyctl.exe"), launcherSource("windows"), {
    encoding: "utf8",
    mode: 0o755
  });
}

await writeFile(
  join(outputDir, "README.md"),
  `# StoryMaker Platform Launchers

Generated by \`corepack pnpm package:binaries\`. The command name is historical; it writes launchers, not native single-file binaries.

These artifacts are deterministic Node-dependent platform launchers for the StoryMaker CLI compatibility entrypoint:

- \`storyctl.exe\`
- \`storyctl-macos\`
- \`storyctl-linux\`

The filenames keep \`storyctl\` for compatibility with existing StoryOS automation. They do not contain Node.js or the CLI source. They require Node.js 20 or newer on \`PATH\` and execute \`packages/cli/dist/index.js\`.
`,
  "utf8"
);

console.log(`Wrote StoryMaker platform launchers to ${outputDir}`);
